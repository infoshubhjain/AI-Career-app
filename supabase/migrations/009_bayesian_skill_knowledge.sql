alter table public.agent_quizzes
  add column if not exists project_id uuid references public.projects(id) on delete cascade;

update public.agent_quizzes q
set project_id = s.project_id
from public.agent_sessions s
where s.id = q.session_id
  and q.project_id is null;

alter table public.agent_quiz_attempts
  add column if not exists project_id uuid references public.projects(id) on delete cascade,
  add column if not exists skill_id text,
  add column if not exists posterior_before double precision,
  add column if not exists posterior_after double precision,
  add column if not exists selection_score double precision,
  add column if not exists model_version text;

update public.agent_quiz_attempts a
set
  project_id = s.project_id,
  skill_id = q.skill_id
from public.agent_sessions s,
     public.agent_quizzes q
where s.id = a.session_id
  and q.id = a.quiz_id
  and (a.project_id is null or a.skill_id is null);

create table if not exists public.user_skill_knowledge (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete cascade not null,
  skill_id text not null,
  domain_id text,
  p_know double precision not null default 0.5,
  p_init double precision not null default 0.5,
  p_learn double precision not null default 0.15,
  p_guess double precision not null default 0.2,
  p_slip double precision not null default 0.1,
  observation_count integer not null default 0,
  correct_count integer not null default 0,
  incorrect_count integer not null default 0,
  last_quiz_id uuid references public.agent_quizzes(id) on delete set null,
  last_attempt_id bigint references public.agent_quiz_attempts(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint user_skill_knowledge_unique unique (user_id, skill_id),
  constraint user_skill_knowledge_probabilities_check check (
    p_know between 0 and 1 and
    p_init between 0 and 1 and
    p_learn between 0 and 1 and
    p_guess between 0 and 1 and
    p_slip between 0 and 1
  )
);

alter table public.user_skill_knowledge enable row level security;

