  attempt_id bigint references public.agent_quiz_attempts(id) on delete cascade,
  skill_id text not null,
  domain_id text,
  is_correct boolean not null,
  posterior_before double precision not null,
  posterior_after double precision not null,
  selection_reason text not null default '',
  selection_score double precision,
  model_version text not null default 'bkt-skill-v1',
  created_at timestamptz not null default now(),
  constraint user_skill_observations_probabilities_check check (
    posterior_before between 0 and 1 and
    posterior_after between 0 and 1
  )
);

