create table public.agent_events (
  id bigint generated by default as identity primary key,
  session_id uuid references public.agent_sessions(id) on delete cascade not null,
  role text not null,
  agent text not null,
  event_type text not null,
  content text,
  payload jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.agent_events enable row level security;

create policy "Users can view their own agent events." on public.agent_events
  for select using (
    exists (
      select 1
      from public.agent_sessions s
      where s.id = session_id and s.user_id = auth.uid()
    )
  );

create policy "Users can insert their own agent events." on public.agent_events
  for insert with check (
    exists (
      select 1
      from public.agent_sessions s
      where s.id = session_id and s.user_id = auth.uid()
    )
  );

drop trigger if exists set_agent_events_updated_at on public.agent_events;
create trigger set_agent_events_updated_at
  before update on public.agent_events
  for each row execute procedure public.touch_profile_updated_at();

