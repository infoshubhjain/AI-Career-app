alter table public.profiles
  add column if not exists reading_level text;

drop table if exists public.agent_quiz_attempts cascade;
drop table if exists public.agent_quizzes cascade;
drop table if exists public.agent_memories cascade;
drop table if exists public.agent_events cascade;
drop table if exists public.agent_sessions cascade;
drop table if exists public.projects cascade;

create table public.projects (
  id uuid primary key default gen_random_uuid(),
  user_id uuid references public.profiles(id) on delete cascade not null,
  title text not null,
  goal text not null,
  roadmap_json jsonb not null,
  status text not null default 'active',
  latest_session_id uuid,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.projects enable row level security;

create policy "Users can view their own projects." on public.projects
  for select using (auth.uid() = user_id);

create policy "Users can insert their own projects." on public.projects
  for insert with check (auth.uid() = user_id);

create policy "Users can update their own projects." on public.projects
  for update using (auth.uid() = user_id);

drop trigger if exists set_projects_updated_at on public.projects;
create trigger set_projects_updated_at
  before update on public.projects
  for each row execute procedure public.touch_profile_updated_at();

